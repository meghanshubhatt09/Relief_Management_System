    private void btnSendRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendRequestActionPerformed

        // TODO add your handling code here:

        if(txtPeopleCount.getText().isEmpty() || txtVenue.getText().isEmpty()){
        JOptionPane.showMessageDialog(null, "Please enter all the values", "Warning", JOptionPane.WARNING_MESSAGE);
        return;
        }
        CommunityPoliceRequest request = new CommunityPoliceRequest();

       // request.setNoDoctorRequired(Integer.parseInt(txtDoctorCount.getText()));
        request.setLocation(txtVenue.getText());
        Date requestDate = new Date();
        request.setRequestDate(requestDate);
        //                request.setRequestedTime(txtTime.getText());
       // request.setPurpose(txtRequestType.getText());
        request.setStatus("Requested");
        request.setSender(account);
        
        try{
                request.setNoPeopleAffected(Integer.parseInt(txtPeopleCount.getText()));

        }catch(NumberFormatException e){
        JOptionPane.showMessageDialog(null, "Please enter numeric value", "Warning", JOptionPane.WARNING_MESSAGE);
        return;
        }
        
        request.setPoliceType(doctorTypeComboBox.getSelectedItem().toString());
        request.setPurpose(emergencyTypeComboBox.getSelectedItem().toString());
        account.getWorkQueue().getWorkRequestList().add(request);
        enterprise.getWorkQueue().getWorkRequestList().add(request);
        ecoSystem.getWorkQueue().getWorkRequestList().add(request);

       populateRequestTable();

        //            JOptionPane.showMessageDialog(null,"Enter date in specified format", "Warning", JOptionPane.WARNING_MESSAGE);

    }//GEN-LAST:event_btnSendRequestActionPerformed

    
    private void populateRequestTable(){
        DefaultTableModel model = (DefaultTableModel) tblRequest.getModel();
        model.setRowCount(0);
        for(WorkRequest work : ecoSystem.getWorkQueue().getWorkRequestList()){
            if(work instanceof CommunityPoliceRequest && work.getSender() == account){
                Object[] row = new Object[10];
                row[0] = ((CommunityPoliceRequest) work).getPoliceCount() ;
                row[1] = ((CommunityPoliceRequest) work).getNoPeopleAffected() ;
                row[2] = ((CommunityPoliceRequest) work).getRequestDate() ;
                row[3] = ((CommunityPoliceRequest) work).getResolveDate() ;
                row[7] = work;
                row[4] = ((CommunityPoliceRequest) work).getLocation() ;
                row[5] = ((CommunityPoliceRequest) work).getPoliceType();
                row[6] = ((CommunityPoliceRequest) work).getPurpose() ;
                row[8] = ((CommunityPoliceRequest) work).getSender();
                if(((CommunityPoliceRequest) work).getReceiver() != null){
                     row[9] = ((CommunityPoliceRequest) work).getReceiver().getUsername() ;
                }
               

                model.addRow(row);
            
            }
        }
        
    }
    
    
    
